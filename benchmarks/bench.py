#!/usr/bin/env python3
"""Real, measured benchmarks for the solver and generator.

Run with: python3 benchmarks/bench.py
Writes results to reports/BENCHMARKS.md (created if missing).
No numbers in this file or the README are invented -- everything here is
regenerated by actually running the code.
"""

from __future__ import annotations

import os
import random
import sys
import time

sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))

from sudoku.board import parse
from sudoku.generator import DIFFICULTY_TARGET_CLUES, generate_puzzle
from sudoku.solver import solve

KNOWN_PUZZLES = {
    "AI Escargot (Arto Inkala, 2006 -- famously not solvable by singles alone)": (
        "1.......2.9.4...5...6...7...5.9.3.......7.......85..4.7.....6...3...9.8...2.....1"
    ),
    "World's Hardest Sudoku (Arto Inkala, 2012)": (
        "8..........36......7..9.2...5...7.......457.....1...3...1....68..85...1..9....4.."
    ),
    "Empty board (81 blanks -- worst case for search depth)": "." * 81,
}


def bench_known_puzzles() -> list[str]:
    lines = ["### Solve time on known hard puzzles", "", "| Puzzle | Solve time |", "|---|---|"]
    for name, text in KNOWN_PUZZLES.items():
        cells = parse(text)
        start = time.perf_counter()
        result = solve(cells)
        elapsed_ms = (time.perf_counter() - start) * 1000
        assert result is not None
        lines.append(f"| {name} | {elapsed_ms:.2f} ms |")
    lines.append("")
    return lines


def bench_generation() -> list[str]:
    lines = [
        "### Generation time + achieved clue count by difficulty",
        "",
        "Averaged over 10 seeded runs per difficulty.",
        "",
        "| Difficulty | Avg generation time | Avg clues remaining | Rated-difficulty distribution |",
        "|---|---|---|---|",
    ]
    for difficulty in DIFFICULTY_TARGET_CLUES:
        times = []
        clue_counts = []
        ratings: dict[str, int] = {}
        for seed in range(10):
            start = time.perf_counter()
            puzzle, _, actual = generate_puzzle(difficulty=difficulty, seed=seed)
            times.append((time.perf_counter() - start) * 1000)
            clue_counts.append(sum(1 for v in puzzle if v != 0))
            ratings[actual] = ratings.get(actual, 0) + 1
        avg_time = sum(times) / len(times)
        avg_clues = sum(clue_counts) / len(clue_counts)
        dist = ", ".join(f"{k}:{v}" for k, v in sorted(ratings.items()))
        lines.append(f"| {difficulty} (target {DIFFICULTY_TARGET_CLUES[difficulty]} clues) | {avg_time:.1f} ms | {avg_clues:.1f} | {dist} |")
    lines.append("")
    return lines


def main() -> None:
    header = [
        "# Sudoku Solver/Generator -- Benchmark Results",
        "",
        f"Regenerated by `benchmarks/bench.py`. Python {sys.version.split()[0]}, single-threaded, CPython, no PyPy/JIT.",
        "",
    ]
    body = bench_known_puzzles() + bench_generation()

    report_dir = os.path.join(os.path.dirname(__file__), "..", "reports")
    os.makedirs(report_dir, exist_ok=True)
    out_path = os.path.join(report_dir, "BENCHMARKS.md")
    with open(out_path, "w") as f:
        f.write("\n".join(header + body))

    print("\n".join(header + body))
    print(f"\nWritten to {out_path}")


if __name__ == "__main__":
    main()
